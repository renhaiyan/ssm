package com.self.pojo.task;

public class Task {

	private String id;
	private String taskType;
	private String executeDate;
	private String executeUser;
	private String title;
	private String link;
	private String printscreen;
	private String exenum;
	private String unit;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTaskType() {
		return taskType;
	}
	public void setTaskType(String taskType) {
		this.taskType = taskType;
	}
	public String getExecuteDate() {
		return executeDate;
	}
	public void setExecuteDate(String executeDate) {
		this.executeDate = executeDate;
	}
	public String getExecuteUser() {
		return executeUser;
	}
	public void setExecuteUser(String executeUser) {
		this.executeUser = executeUser;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getPrintscreen() {
		return printscreen;
	}
	public void setPrintscreen(String printscreen) {
		this.printscreen = printscreen;
	}
	public String getExenum() {
		return exenum;
	}
	public void setExenum(String exenum) {
		this.exenum = exenum;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
}
